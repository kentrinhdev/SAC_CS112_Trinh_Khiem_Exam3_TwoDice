// Display Welcome message when user starts the game
public class Welcome 
{
	// 3 Move objects: Rock, Paper, Scissors
	String introMessage;
	String gameName;
	String messageInfo;
	
	// Constructor with (variables) to pass values
	public Welcome( String intro, String game, String message ) 
	{
		introMessage = intro;
		gameName = game;
		messageInfo = message;
	} // end constructor Welcome
	
} // end class Welcome
